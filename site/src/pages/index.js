function change_event(event) {
  let clicked = event.target.classList.length === 2;
  let likes = event.target.parentElement.nextElementSibling.innerText;
  event.target.parentElement.nextElementSibling.innerText = clicked
    ? parseInt(likes) + 1
    : parseInt(likes) - 1;
}

